// FOR THE PROJECT PAGINATION
//=========================================
const projectCard = document.querySelectorAll("#projectSection > div");

let numAllowed = 6;
let numNotAllowed = -6;
let highNum = 0;
let lowNum = -6;

function pagination(e) {
  highNum = highNum + e;
  lowNum = lowNum + e;
  for (let i = 0; i < projectCard.length; i++) {
    const element = projectCard[i];
    const elemCount = i * 1 + 1;
    if (elemCount > highNum || elemCount <= lowNum) {
      element.style.display = "none";
    } else {
      element.style.display = "block";
    }
  }
}

let next = () => [pagination(numAllowed), btns()];
let back = () => [pagination(numNotAllowed), btns()];
pagination(numAllowed);
